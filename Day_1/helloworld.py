# Arithmetic Operators
print(3 + 2)   # addition(+)
print(3 - 2)   # subtraction(-)
print(3 * 2)   # multiplication(*)
print(3 / 2)   # division(/)
print(3 ** 2)  # exponential(**)
print(3 % 2)   # modulus(%)
print(3 // 2)  # Floor division operator(//)

# Shortcut Operators
print("+=")  # Addition and assign
print("-=")  # Subtraction and assign
print("*=")  # Multiplication and assign
print("/=")  # Division and assign
print("%=")  # Modulus and assign
print("**=") # Exponent and assign
print("//=") # Floor Division and assign

# Data Types
print(type(12))                  # Int 
print(type(0.90))                # Float 
print(type('Pepsi'))             # String 
print(type(['Orange', 'Apple'])) # List 
print(type({'City':'London'}))   # Dictionary
print(type({1, 2, 3, 4, 5}))     # Set
print(type((34, 22, 15, 45)))    # Tuple
print(type(True))                # Bool 
